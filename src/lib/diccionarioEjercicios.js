// 🔥 LA FUNCIÓN SIGUE INTACTA
export function analizarRutina(textoDeLaIA) {
  if (!textoDeLaIA) return [];

  const textoLimpio = textoDeLaIA
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");

  const ejerciciosEncontrados = [];

  catalogoEjercicios.forEach((ejercicio) => {
    const coincide = ejercicio.palabrasClave.some(clave => textoLimpio.includes(clave));
    if (coincide) {
      if (!ejerciciosEncontrados.some(e => e.nombre === ejercicio.nombre)) {
        ejerciciosEncontrados.push(ejercicio);
      }
    }
  });

  return ejerciciosEncontrados;
}